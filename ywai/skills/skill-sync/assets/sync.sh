#!/usr/bin/env bash
# Sync skill metadata to AGENTS.md Auto-invoke sections (Ultra robust version)
#
# Modes:
#   (default)   - sync Auto-invoke tables in AGENTS.md files
#   --registry  - generate .ywai/skill-registry.md with compact rules per skill
#   --dry-run   - preview changes without writing
#   --scope X   - restrict to a specific scope

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../../../" && pwd)"
SKILLS_DIR="$REPO_ROOT/skills"

MODE="sync"
DRY_RUN=0
SCOPE_FILTER=""
for arg in "$@"; do
    case "$arg" in
        --registry) MODE="registry" ;;
        --dry-run) DRY_RUN=1 ;;
        --scope) shift; SCOPE_FILTER="$1" ;;
        --scope=*) SCOPE_FILTER="${arg#--scope=}" ;;
    esac
done

# ---------------------------------------------------------------------------
# Compact rules extractor
# ---------------------------------------------------------------------------
# Extracts 5-15 actionable bullets from a SKILL.md, preferring sections named
# "## Critical Patterns", "## Rules", or "## Critical Rules". Falls back to
# the first bullet list encountered. Each bullet is trimmed to 160 chars.
extract_rules() {
    local file="$1"
    awk '
        BEGIN { collecting=0; count=0 }
        /^##[[:space:]]+(Critical[[:space:]]Patterns|Rules|Critical[[:space:]]Rules)[[:space:]]*$/ {
            collecting=1; next
        }
        collecting && /^## / { collecting=0 }
        collecting && /^[[:space:]]*-[[:space:]]/ {
            if (count < 15) {
                line=$0
                sub(/^[[:space:]]*-[[:space:]]*/, "", line)
                if (length(line) > 160) line = substr(line, 1, 157) "..."
                print "- " line
                count++
            }
        }
    ' "$file"
}

extract_triggers() {
    # Pulls Trigger: "a", "b", "c" from description block.
    local file="$1"
    awk '
        /^description:/,/^[a-zA-Z_]+:/ {
            if (match($0, /[Tt]rigger[s]?:/)) { trig=1 }
            if (trig) print
        }
    ' "$file" | grep -oE '"[^"]+"' | paste -sd, - 2>/dev/null | head -n1
}

get_name() {
    local file="$1"
    grep -m1 '^name:' "$file" | cut -d':' -f2- | tr -d '"' | xargs
}

get_version() {
    local file="$1"
    grep -m1 '^[[:space:]]*version:' "$file" | cut -d':' -f2- | tr -d '"' | xargs
}

get_scopes() {
    local file="$1"
    local raw
    raw=$(grep -A1 '^[[:space:]]*scope:' "$file" | head -n2)
    if echo "$raw" | grep -q '\['; then
        echo "$raw" | grep -oE '\[[^]]+\]' | head -n1 | tr -d '[]' | tr ',' ' '
    else
        echo "root"
    fi
}

generate_registry() {
    local out_dir="$REPO_ROOT/.ywai"
    local out="$out_dir/skill-registry.md"
    mkdir -p "$out_dir"

    if [ "$DRY_RUN" -eq 1 ]; then
        echo "[dry-run] would write $out"
        out="/dev/stdout"
    fi

    {
        echo "# Skill Registry (compact rules)"
        echo ""
        echo "_Auto-generated by \`skills/skill-sync/assets/sync.sh --registry\`. Do not edit by hand._"
        echo ""
        echo "_Sub-agents: read this file instead of individual SKILL.md to get project-wide rules._"
        echo ""
        local count=0
        while IFS= read -r skill_file; do
            [ -f "$skill_file" ] || continue
            local name triggers scopes version rules rel
            name=$(get_name "$skill_file")
            [ -z "$name" ] && continue
            triggers=$(extract_triggers "$skill_file")
            scopes=$(get_scopes "$skill_file" | xargs)
            version=$(get_version "$skill_file")
            rules=$(extract_rules "$skill_file")
            rel="${skill_file#$REPO_ROOT/}"
            [ -z "$rules" ] && continue
            count=$((count+1))
            echo "## skill: $name"
            [ -n "$triggers" ] && echo "triggers: [$triggers]"
            echo "scope: [$(echo "$scopes" | tr ' ' ',' | sed 's/,/, /g')]"
            [ -n "$version" ] && echo "version: $version"
            echo "source: $rel"
            echo "rules:"
            echo "$rules"
            echo ""
        done < <(find "$SKILLS_DIR" -maxdepth 3 -name SKILL.md -print | sort)
        echo "<!-- $count skills indexed -->"
    } > "$out"

    if [ "$DRY_RUN" -eq 0 ]; then
        echo "Wrote $out"
    fi
}

if [ "$MODE" = "registry" ]; then
    echo "Skill Registry (compact rules)"
    generate_registry
    exit 0
fi

resolve_root_agents_md() {
    local found
    found=$(find "$REPO_ROOT" -maxdepth 1 -type f -iname "AGENTS.md" | head -n 1)
    if [ -n "$found" ]; then
        echo "$found"
    else
        echo "$REPO_ROOT/AGENTS.md"
    fi
}

get_agents_path() {
    local scope="$1"
    local agents_md
    agents_md="$(resolve_root_agents_md)"
    case "$scope" in
        root) echo "$agents_md" ;;
        copilot) echo "$REPO_ROOT/.github/copilot-instructions.md" ;;
        *) 
            # Auto-detect AGENTS.md in subdirectories matching the scope
            # Try exact match first, then case-insensitive
            local found=$(find "$REPO_ROOT" -maxdepth 5 -type f -iname "AGENTS.md" | 
                grep -i "$scope" | head -n 1)
            [ -n "$found" ] && echo "$found" || echo ""
            ;;
    esac
}

extract_fm() {
    local file="$1"
    # Check if file starts with ---
    if head -n 1 "$file" | tr -d '\r' | grep -q "^---$"; then
        # Standard YAML with --- delimiters
        awk 'BEGIN {n=0} /^---(\r)?$/ {n++; next} n==1 {print} n==2 {exit}' "$file"
    else
        # Non-delimited YAML - extract until first ---
        awk '/^---(\r)?$/ {exit} {print}' "$file"
    fi
}

echo "Skill Sync (Bash/Awk/Sed)"

# Use a temp file to store mappings: scope|action|skill_name
temp_map=$(mktemp)

while IFS= read -r skill_file; do
    [ -f "$skill_file" ] || continue
    
    fm=$(extract_fm "$skill_file")
    [ -z "$fm" ] && continue

    # Clean FM from potential \r and trailing spaces
    fm=$(echo "$fm" | tr -d '\r')

    name=$(echo "$fm" | grep "^name:" | head -n 1 | cut -d':' -f2- | tr -d '[]"'\''' | xargs)
    # If not found directly, look for name inside metadata: block
    [ -z "$name" ] && name=$(echo "$fm" | sed -n '/metadata:/,/^[a-z]/p' | grep "^[[:space:]]*name:" | head -n 1 | cut -d':' -f2- | tr -d '[]"'\''' | xargs)
    
    # Extract scopes - support inline [a, b] and multiline - a
    scopes_raw=$(echo "$fm" | grep -A 5 "^[[:space:]]*scope:")
    if echo "$scopes_raw" | grep -q "\["; then
        scopes=$(echo "$scopes_raw" | grep "scope:" | cut -d'[' -f2 | cut -d']' -f1 | tr ',' ' ')
    else
        scopes=$(echo "$scopes_raw" | sed -n '/scope:/,/^[a-z]/p' | grep "^[[:space:]]*-" | sed 's/^[[:space:]]*-//' | xargs)
    fi
    
    # Extract auto_invoke - support inline and multiline
    actions_raw=$(echo "$fm" | grep -A 10 "^[[:space:]]*auto_invoke:")
    auto_invoke_line=$(echo "$actions_raw" | grep "auto_invoke:" | head -n 1)
    if echo "$auto_invoke_line" | grep -q "\["; then
        actions=$(echo "$auto_invoke_line" | cut -d'[' -f2 | cut -d']' -f1 | tr ',' '\n')
    else
        actions=$(echo "$actions_raw" | sed -n '/auto_invoke:/,/^[a-z]/p' | grep "^[[:space:]]*-" | sed 's/^[[:space:]]*-//')
    fi
    
    [ -z "$name" ] || [ -z "$scopes" ] || [ -z "$actions" ] && {
        # echo "Skipping $skill_file: missing name($name), scope($scopes) or actions"
        continue
    }
    
    echo "Processing skill: $name (Scopes: $scopes)"

    for s in $scopes; do
        s=$(echo "$s" | tr -d '[:space:],')
        [ -z "$s" ] && continue

        echo "$actions" | while read -r a; do
            a=$(echo "$a" | sed 's/^[[:space:]]*//;s/[[:space:]]*$//' | tr -d '"' | tr -d "'")
            [ -n "$a" ] && echo "$s|$a|$name" >> "$temp_map"
        done
    done
done < <(find "$SKILLS_DIR" -maxdepth 3 -name SKILL.md -print)

# Now read the temp_map and update AGENTS.md
# Get unique scopes from temp_map
unique_scopes=$(cut -d'|' -f1 "$temp_map" | sort -u)
for scope in $unique_scopes; do
    paths=()
    if [ "$scope" == "root" ]; then
        paths+=($(get_agents_path "root"))
        paths+=($(get_agents_path "copilot"))
    else
        paths+=($(get_agents_path "$scope"))
    fi

    for agents_path in "${paths[@]}"; do
        [ -z "$agents_path" ] || [ ! -f "$agents_path" ] && continue
        
        table="## Auto-invoke Capabilities
| Action | Required Skill | Trigger Pattern |
| :--- | :--- | :--- |"
        
        # Filter temp_map for current scope and format rows
        sorted_rows=$(grep "^$scope|" "$temp_map" | cut -d'|' -f2- | sort -u | while IFS='|' read -r a n; do
            echo "| $a | \`$n\` | $a |"
        done)
        
        # Only update if we have rows or it's root (to keep it clean)
        if [ -n "$sorted_rows" ] || [ "$scope" == "root" ]; then
            echo "Updating $agents_path"
            # Form clean replacement section
            full_section="$table\n$sorted_rows"
            
            # Check if Auto-invoke section exists
            if grep -q "^##.*Auto-invoke" "$agents_path"; then
                # Replace existing section
                tmp_file=$(mktemp)
                awk -v section="$full_section" '
                    BEGIN { skip=0; printed=0 }
                    /^## (.* )?Auto-invoke/ {
                        if (!printed) {
                            print section
                            printed=1
                        }
                        skip=1
                        next
                    }
                    skip && /^## / { skip=0 }
                    !skip { print }
                ' "$agents_path" > "$tmp_file"
                mv "$tmp_file" "$agents_path"
            else
                # Insert new section after "Part 5: Available Skills" or at end of file
                tmp_file=$(mktemp)
                awk -v section="$full_section" '
                    BEGIN { inserted=0 }
                    /^## Part 6: SDD Orchestrator/ {
                        if (!inserted) {
                            print section
                            inserted=1
                        }
                    }
                    /^## Engram Persistent Memory/ {
                        if (!inserted) {
                            print section
                            inserted=1
                        }
                    }
                    { print }
                    END {
                        if (!inserted) {
                            print "\n" section
                        }
                    }
                ' "$agents_path" > "$tmp_file"
                mv "$tmp_file" "$agents_path"
            fi
        fi
    done
done

rm "$temp_map"
echo "Done!"
